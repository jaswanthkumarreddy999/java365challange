import java.util.Scanner;

public class add {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println();
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a+b);
        float c = sc.nextFloat();
        float d = sc.nextFloat();
        System.out.println(c+d);
    }
}